const fs = require('fs');
const path = require('path');

const projectsDir = path.join(__dirname, '..', 'src', 'assets', 'projects');
const outputFilePath = path.join(__dirname, '..', 'src', 'data', 'projects.ts');

const folders = fs.readdirSync(projectsDir).filter(f => fs.statSync(path.join(projectsDir, f)).isDirectory());

let imports = '';
let projectsArray = [];

folders.forEach((folder, idx) => {
  const dirPath = path.join(projectsDir, folder);
  const metadataPath = path.join(dirPath, 'metadata.json');
  const mdxPath = path.join(dirPath, 'description.mdx');

  if (!fs.existsSync(metadataPath)) return;

  const metadata = JSON.parse(fs.readFileSync(metadataPath, 'utf8'));
  const mdxContent = fs.existsSync(mdxPath) ? fs.readFileSync(mdxPath, 'utf8') : '';

  // Get image and video files strictly in the project-specific 'media' directory
  const mediaDirPath = path.join(dirPath, 'media');
  if (!fs.existsSync(mediaDirPath)) return;

  const files = fs.readdirSync(mediaDirPath);
  
  const imageExtensions = ['.png', '.jpg', '.jpeg', '.gif', '.svg', '.webp'];
  const images = files.filter(file => imageExtensions.includes(path.extname(file).toLowerCase())).sort();

  const videoExtensions = ['.mp4', '.webm', '.ogg', '.mov'];
  const videos = files.filter(file => videoExtensions.includes(path.extname(file).toLowerCase())).sort();

  // Create import statements for each image
  const imageImportsMap = {};
  images.forEach((img) => {
    const cleanFolderName = folder.replace(/[^a-zA-Z0-9]/g, '');
    const cleanImgName = path.basename(img, path.extname(img)).replace(/[^a-zA-Z0-9]/g, '');
    const importName = `${cleanFolderName}_${cleanImgName}`;
    imports += `import ${importName} from "@/assets/projects/${folder}/media/${img}";\n`;
    imageImportsMap[img] = importName;
  });

  // Copy videos to public folder for static serving
  const videosArray = [];
  if (videos.length > 0) {
    const publicProjMediaDir = path.join(__dirname, '..', 'public', 'projects', folder, 'media');
    fs.mkdirSync(publicProjMediaDir, { recursive: true });

    videos.forEach((vid) => {
      const srcPath = path.join(mediaDirPath, vid);
      const destPath = path.join(publicProjMediaDir, vid);
      
      let shouldCopy = true;
      if (fs.existsSync(destPath)) {
        const srcStat = fs.statSync(srcPath);
        const destStat = fs.statSync(destPath);
        if (srcStat.size === destStat.size) {
          shouldCopy = false;
        }
      }
      
      if (shouldCopy) {
        fs.copyFileSync(srcPath, destPath);
        console.log(`Copied video to public: ${folder}/media/${vid}`);
      }

      videosArray.push(`"/projects/${folder}/media/${vid}"`);
    });
  }

  // Identify thumbnail (preview or top-down render, or first image)
  const thumbnailFile = images.find(img => img.toLowerCase().includes('top') || img.toLowerCase().includes('preview') || img.toLowerCase().includes('setup')) || images[0];
  const thumbnailImport = imageImportsMap[thumbnailFile] || null;
  const imagesImportsArray = images.map(img => imageImportsMap[img]);

  projectsArray.push(`  {
    slug: ${JSON.stringify(folder)},
    priority: ${JSON.stringify(metadata.priority)},
    title: ${JSON.stringify(metadata.title)},
    tagline: ${JSON.stringify(metadata.tagline)},
    role: ${JSON.stringify(metadata.role)},
    publishedDate: ${JSON.stringify(metadata.publishedDate)},
    thumbnail: ${thumbnailImport},
    images: [${imagesImportsArray.join(', ')}],
    videos: [${videosArray.join(', ')}],
    specs: ${JSON.stringify(metadata.specs, null, 2).replace(/\n/g, '\n    ')},
    techStack: ${JSON.stringify(metadata.techStack)},
    links: ${JSON.stringify(metadata.links, null, 2).replace(/\n/g, '\n    ')},
    description: ${JSON.stringify(mdxContent)}
  }`);
});

const fileContent = `// This file is auto-generated by scripts/compile-projects.js
// Do not edit this file directly.

${imports}
export interface ProjectMetadata {
  slug: string;
  priority: "flagship" | "standard";
  title: string;
  tagline: string;
  role: string;
  publishedDate: string;
  thumbnail: any; // Static Next.js Image Imports
  images: any[]; // Supporting high-res gallery assets
  videos: string[]; // Supporting high-res video assets
  specs: { label: string; value: string }[];
  techStack: string[];
  links: {
    github?: string;
    workspace?: string;
  };
  description: string; // MDX-style formatted text body
}

export const projects: ProjectMetadata[] = [
${projectsArray.join(',\n')}
];
`;

fs.writeFileSync(outputFilePath, fileContent, 'utf8');
console.log('Successfully generated src/data/projects.ts');
